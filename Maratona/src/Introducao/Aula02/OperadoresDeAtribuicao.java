package Introducao.Aula02;

public class OperadoresDeAtribuicao {
    public static void main(String[] args) {

        // = , -=, +=, *=, /=,  %=

        int salary = 1000;
        // more extense way
        salary = salary + 1000;

        // using the Attribution Operator +=
        salary += 1000;

        System.out.println(salary);
    }
}
