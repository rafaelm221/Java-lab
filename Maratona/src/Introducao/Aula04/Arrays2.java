package Introducao.Aula04;

public class Arrays2 {
    public static void main(String[] args) {
        int[] numero = new int[]{1, 2, 3, 4, 5};

        for (int aux : numero) {
            System.out.println(aux);
        }


        String[] names = {"Juju", "joao", "Maria", "Rafael"};

        for (String aux : names) {
            System.out.println(aux);
        }
    }
}
